/**
 * Memory Core - Oracle Dreaming Artifacts
 */

import oracledb from "oracledb";

// ========================================================================
// Config
// ========================================================================

let pool: oracledb.Pool | null = null;

export async function initOracle(config: {
  user: string;
  password: string;
  connectString: string;
}) {
  pool = await oracledb.createPool(config);
}

function getConn(): Promise<oracledb.Connection> {
  if (!pool) throw new Error("Oracle not initialized");
  return pool.getConnection();
}

// ========================================================================
// TABLE
// ========================================================================

const TABLES = [
  `CREATE TABLE dreaming_diary (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    content CLOB NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
  )`,
  `CREATE TABLE dreaming_session_corpus (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    content CLOB NOT NULL,
    is_suspicious NUMBER(1) DEFAULT 0,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
  )`,
  `CREATE TABLE dreaming_session_ingestion (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    data CLOB NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
  )`,
];

async function ensureTables() {
  const conn = await getConn();
  for (const sql of TABLES) {
    try {
      await conn.execute(sql);
    } catch (e: any) {
      if (e.errorNum !== 955) throw e; // ORA-955
    }
  }
  await conn.close();
}

// ========================================================================
// API
// ========================================================================

export async function auditDreamingArtifacts() {
  await ensureTables();
  const conn = await getConn();

  const diary = await conn.execute(`SELECT COUNT(*) FROM dreaming_diary`);
  const corpus = await conn.execute(`SELECT COUNT(*) FROM dreaming_session_corpus`);
  const ingestion = await conn.execute(`SELECT COUNT(*) FROM dreaming_session_ingestion`);

  await conn.close();

  return {
    hasDiary: (diary.rows?.[0]?.[0] as number) > 0,
    hasCorpus: (corpus.rows?.[0]?.[0] as number) > 0,
    hasIngestion: (ingestion.rows?.[0]?.[0] as number) > 0,
  };
}

export async function repairDreamingArtifacts() {
  await ensureTables();
  const conn = await getConn();

  await conn.execute(`DELETE FROM dreaming_diary`);
  await conn.execute(`DELETE FROM dreaming_session_corpus`);
  await conn.execute(`DELETE FROM dreaming_session_ingestion`);

  await conn.close();

  return { cleaned: true };
}